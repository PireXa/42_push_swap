#include "push_swap.h"

t_stack	*ft_lstlast(t_stack *lst)
{
	t_stack	*temp;

	if (!lst)
		return (NULL);
	temp = lst;
	while (temp -> next != NULL)
		temp = temp -> next;
	return (temp);
}
